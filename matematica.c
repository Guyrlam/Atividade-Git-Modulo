#include <stdio.h>
#include <math.h>
#include "matematica.h"

float calcular_seno(float angulo_radianos)
{
    float seno_angulo = sin(angulo_radianos); 

    return seno_angulo;
};

float calcular_cosseno(float angulo_radianos)
{
    float cosseno_angulo = cos(angulo_radianos); 

    return cosseno_angulo;
};

float calcular_tangente(float angulo_radianos)
{
    float tangente_angulo = tan(angulo_radianos); 

    return tangente_angulo;
};
